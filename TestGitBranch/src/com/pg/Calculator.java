package com.pg;

/*This is class does basic operations */
public class Calculator {
	//This method adds two numbers
	public  int add (int x, int y) {
		return x+y;
	}
	
	public int sub (int x, int y){
		return x-y;
	}
	
	
	//sub method
	public int mul (int x, int y){
		return x*y;
	}

	//div method
	public double div (int x, int y){
		return x/y;
	}

}
